public class Variables {
    public static void main(String[] args) {
        String name = "Болд";
        System.out.println(name);

        int age = 25;
        System.out.println(age);

        float height = 1.75f;
        System.out.println(height);

        char grade = 'A';
        System.out.println(grade);

        boolean isActive = true;
        System.out.println(isActive);
    }
}

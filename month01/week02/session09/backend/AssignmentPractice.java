public class AssignmentPractice {
    public static void main(String[] args) {
        int x = 100;

        x += 20;   // x = x + 20 = ___
        System.out.println("x += 20: " + x);

        x -= 30;   // x = x - 30 = ___
        System.out.println("x -= 30: " + x);

        x *= 2;    // x = x * 2 = ___
        System.out.println("x *= 2: " + x);

        x /= 9;    // x = x / 9 = ___
        System.out.println("x /= 9: " + x);

        x %= 5;    // x = x % 5 = ___
        System.out.println("x %= 5: " + x);
    }
}

